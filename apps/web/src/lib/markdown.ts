/**
 * 가벼운 마크다운 → 평문 변환. 새 의존성 없이 기본 문법만 정리한다.
 * 회의록 원문 미리보기에서 `**볼드**`·`#`·`- ` 같은 문법이 그대로 노출되는 것을 막는다.
 * 원문 데이터 자체는 보존되며, 표시용으로만 정리한다.
 */
export function stripMarkdown(md: string): string {
  return md
    .split("\n")
    .map((line) =>
      line
        // 헤딩 마커 제거 (# ~ ######)
        .replace(/^\s{0,3}#{1,6}\s+/, "")
        // 인용 마커 제거 (>)
        .replace(/^\s*>\s?/, "")
        // 목록 마커를 가운뎃점으로 통일 (- * +)
        .replace(/^(\s*)[-*+]\s+/, "$1• ")
        // 링크 [텍스트](url) → 텍스트
        .replace(/\[([^\]]+)\]\([^)]*\)/g, "$1")
        // 볼드/이탤릭/코드 마커 제거
        .replace(/\*\*(.+?)\*\*/g, "$1")
        .replace(/__(.+?)__/g, "$1")
        .replace(/\*(.+?)\*/g, "$1")
        .replace(/`([^`]+)`/g, "$1"),
    )
    .join("\n");
}
